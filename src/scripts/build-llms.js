/** Unicode decorative characters (box-drawing, block elements, geometric shapes) */
const DECORATIVE_RE = /[\u2500-\u259F\u25A0-\u25FF]/g;

/** Clusters of ASCII art characters (2 or more consecutive art chars) */
const ART_CLUSTER_RE = /[*#●^~○]{2,}/g;

